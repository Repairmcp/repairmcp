import { McpServer } from '@modelcontextprotocol/sdk/server/mcp.js';
import type { Transport } from '@modelcontextprotocol/sdk/shared/transport.js';
import type { SourceAdapter } from '../adapter/source-adapter.js';
import type { BaseItem } from '../adapter/types.js';
import {
  buildFindSupportingTool,
  buildGetByIdTool,
  buildListRecentTool,
  buildSearchTool,
  type ToolRegistrar,
} from './tool-builder.js';

export interface RepairMCPServerOpts {
  /** Server name advertised over MCP (e.g. "repairmcp-deg"). */
  name: string;
  /** Server version, e.g. package.json version. */
  version: string;
}

/** Names of the four standard tools generated by `registerStandardTools`. */
export type StandardToolName = 'search' | 'get' | 'list_recent' | 'find_supporting';

export interface RegisterStandardToolsOpts {
  /**
   * Skip these standard tools. Useful when a vertical wants to register a
   * domain-specific replacement under the same MCP tool name (e.g. DEG's
   * custom `deg_find_supporting` with bigram + IP scoring).
   */
  skip?: StandardToolName[];
}

/**
 * Thin wrapper around the MCP SDK `McpServer` that knows how to register the
 * four standard tools from any `SourceAdapter`. Verticals can also attach
 * custom tools via `registerCustomTool`.
 */
export class RepairMCPServer<TItem extends BaseItem> {
  private readonly mcpServer: McpServer;

  constructor(
    private readonly adapter: SourceAdapter<TItem>,
    opts: RepairMCPServerOpts,
  ) {
    this.mcpServer = new McpServer(
      { name: opts.name, version: opts.version },
      { capabilities: { tools: {} } },
    );
  }

  /**
   * Register the four standard tools auto-generated from the adapter.
   * Tool names are prefixed with the adapter's `sourceId`.
   *
   * Pass `{ skip: ['find_supporting'] }` (etc.) to omit a standard tool when
   * you intend to register a domain-specific replacement under the same name.
   */
  registerStandardTools(opts: RegisterStandardToolsOpts = {}): this {
    const skip = new Set<StandardToolName>(opts.skip ?? []);
    const pairs: Array<[StandardToolName, ToolRegistrar]> = [
      ['search', buildSearchTool(this.adapter)],
      ['get', buildGetByIdTool(this.adapter)],
      ['list_recent', buildListRecentTool(this.adapter)],
      ['find_supporting', buildFindSupportingTool(this.adapter)],
    ];
    for (const [name, register] of pairs) {
      if (!skip.has(name)) register(this.mcpServer);
    }
    return this;
  }

  /**
   * Register a vertical-specific custom tool (e.g. deg_get_estimating_tip).
   * The registrar is responsible for choosing a unique name; collisions throw.
   */
  registerCustomTool(register: ToolRegistrar): this {
    register(this.mcpServer);
    return this;
  }

  /** Bind to a transport (STDIO, Streamable HTTP, ...) and start listening. */
  async connect(transport: Transport): Promise<void> {
    await this.mcpServer.connect(transport);
  }

  async close(): Promise<void> {
    await this.mcpServer.close();
  }

  /** Escape hatch for advanced features (notifications, custom request handlers). */
  getServer(): McpServer {
    return this.mcpServer;
  }
}
